     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request -> validate([
            'fname' => 'required',
            'lname' => 'required',
            'dob' => 'required',
            'marks' => 'required',
            'parent_id' => 'required',
            'forms_streamCode' => 'required'
        ]);

        Student::create($request->all());

        return redirect()->route('student');
    }

